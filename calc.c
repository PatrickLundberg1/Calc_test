#include "calcfunc.h"

// adds two numbers
float Addition(float n1, float n2){
	return n1 + n2;
}

// subtracts two numbers
float Subtraction(float n1, float n2){
	return n1 - n2;
}

// multiply two numbers
float Multiplication(float n1, float n2){
	return n1 * n2;
}

// divide two numbers
float Division(float n1, float n2){
	return n1 / n2;
}